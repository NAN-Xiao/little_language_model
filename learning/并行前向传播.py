"""
极简演示: Transformer 怎么一次算出所有位置的 logits
运行: python learning/并行前向传播.py

本脚本展示一个3个token的序列，Transformer如何一次性算出所有位置的logits。
核心洞察: 矩阵乘法 (@) 天然并行处理所有行，seq维度始终保留。
"""

import torch
import torch.nn.functional as F

# 设置打印精度，便于观察
torch.set_printoptions(precision=2, sci_mode=False)

# ============================================================================
# 超参数
# ============================================================================
batch = 1
seq = 3          # 3个token: [token_0, token_1, token_2]
d_model = 8      # 模型维度，每个token用8维向量表示
vocab = 5        # 词表大小，共5个不同的词

# ============================================================================
# 输入: 3个token的id
# ============================================================================
input_ids = torch.tensor([[0, 1, 2]])   # shape: (1, 3)
# input_ids[0,0] = 0  → token_0
# input_ids[0,1] = 1  → token_1
# input_ids[0,2] = 2  → token_2

# ============================================================================
# Step 1: Embedding (查表)
# ============================================================================
# embedding 是一个 (vocab, d_model) 的查找表
# 第 i 行就是 token_i 的 d_model 维向量表示
embedding = torch.randn(vocab, d_model)  # shape: (5, 8)

# input_ids 是 [[0, 1, 2]]，embedding[0] 就是 token_0 的向量
# 这一步同时查出3个token的向量，没有循环！
x = embedding[input_ids]                  # shape: (1, 3, 8)
# x[0,0] = embedding[0]  → token_0 的 8 维向量
# x[0,1] = embedding[1]  → token_1 的 8 维向量
# x[0,2] = embedding[2]  → token_2 的 8 维向量

# ============================================================================
# Step 2: Attention Q/K/V 投影
# ============================================================================
# W_q, W_k, W_v 是三个投影矩阵，每个都是 (d_model, d_model)
W_q = torch.randn(d_model, d_model)
W_k = torch.randn(d_model, d_model)
W_v = torch.randn(d_model, d_model)

# x @ W_q.T: (1, 3, 8) @ (8, 8) = (1, 3, 8)
# @ 只操作最后两维，batch和seq原封不动保留
# 结果中 q[0,0], q[0,1], q[0,2] 分别是3个位置的query向量
q = x @ W_q.T    # shape: (1, 3, 8)
k = x @ W_k.T    # shape: (1, 3, 8)
v = x @ W_v.T    # shape: (1, 3, 8)
# 3个位置的 q/k/v 同时算出来，没有循环！

# ============================================================================
# Step 3: 注意力分数 (核心！)
# ============================================================================
# q @ k.transpose(-2, -1):
#   q 的 shape: (1, 3, 8)
#   k.T 的 shape: (1, 8, 3)  (最后两维转置)
#   结果: (1, 3, 3)
#
# scores[0] 是一个 3×3 矩阵：
#   [q0·k0, q0·k1, q0·k2]   ← token_0 对3个位置的分数
#   [q1·k0, q1·k1, q1·k2]   ← token_1 对3个位置的分数
#   [q2·k0, q2·k1, q2·k2]   ← token_2 对3个位置的分数
#
# 3×3=9个分数，一步全算出来！
scores = q @ k.transpose(-2, -1)   # shape: (1, 3, 3)

# ============================================================================
# Step 4: Causal Mask (因果掩码)
# ============================================================================
# causal mask 是一个下三角矩阵，防止当前位置"偷看"未来位置
# mask[i,j] = 1 表示位置 i 可以看到位置 j
# mask[i,j] = 0 表示位置 i 不能看到位置 j
mask = torch.tril(torch.ones(seq, seq))
# mask = [[1, 0, 0],
#         [1, 1, 0],
#         [1, 1, 1]]
#
# 位置0 只能看自己 (mask[0] = [1,0,0])
# 位置1 能看位置0和1 (mask[1] = [1,1,0])
# 位置2 能看所有位置 (mask[2] = [1,1,1])

# 把 mask=0 的位置的分数设为 -inf，softmax后概率为0
scores = scores.masked_fill(mask == 0, float("-inf"))

# ============================================================================
# Step 5: Softmax
# ============================================================================
# 对 scores 的最后一维做 softmax，得到注意力权重
# attn[0,0] 是位置0对3个位置的关注度 (只有位置0有值，其余为0)
# attn[0,1] 是位置1对3个位置的关注度
# attn[0,2] 是位置2对3个位置的关注度
# 3行同时softmax，没有循环！
attn = F.softmax(scores, dim=-1)   # shape: (1, 3, 3)

# ============================================================================
# Step 6: 加权混合 v
# ============================================================================
# attn @ v:
#   attn 的 shape: (1, 3, 3)
#   v 的 shape: (1, 3, 8)
#   结果: (1, 3, 8)
#
# out[0,0] = attn[0,0,0]*v[0,0] + attn[0,0,1]*v[0,1] + attn[0,0,2]*v[0,2]
#          = 1.0*v[0,0] + 0*v[0,1] + 0*v[0,2]
#          = v[0,0]  (位置0只看自己)
#
# out[0,1] = attn[0,1,0]*v[0,0] + attn[0,1,1]*v[0,1] + attn[0,1,2]*v[0,2]
#          (位置1看位置0和1的加权混合)
#
# out[0,2] = attn[0,2,0]*v[0,0] + attn[0,2,1]*v[0,1] + attn[0,2,2]*v[0,2]
#          (位置2看所有位置的加权混合)
#
# 3个位置的attention输出同时算！
out = attn @ v   # shape: (1, 3, 8)

# ============================================================================
# Step 7: FFN (前馈网络，简化版)
# ============================================================================
# FFN 通常是两个线性层: Linear → ReLU → Linear
# 这里简化为两个矩阵乘法
W_ff1 = torch.randn(d_model, d_model * 2)
W_ff2 = torch.randn(d_model * 2, d_model)

ffn = out @ W_ff1   # (1, 3, 8) @ (8, 16) = (1, 3, 16)
ffn = ffn @ W_ff2   # (1, 3, 16) @ (16, 8) = (1, 3, 8)
# 3个位置同时过FFN，seq维度始终保留！

# ============================================================================
# Step 8: Output Projection → Logits
# ============================================================================
# W_proj: (d_model, vocab) = (8, 5)
# logits: (1, 3, 8) @ (8, 5) = (1, 3, 5)
#
# logits[0,0] = (5,)  → 位置0对每个词的分数
# logits[0,1] = (5,)  → 位置1对每个词的分数
# logits[0,2] = (5,)  → 位置2对每个词的分数
#
# 3个位置 × 5个词 = 15个分数，一步全出来！
W_proj = torch.randn(d_model, vocab)
logits = ffn @ W_proj   # shape: (1, 3, 5)

# ============================================================================
# Step 9: CrossEntropyLoss (并行计算所有位置的loss)
# ============================================================================
# labels 是正确答案，告诉每个位置应该预测什么词
labels = torch.tensor([[1, 2, 3]])   # shape: (1, 3)
# labels[0,0] = 1  → 位置0应该预测token_1
# labels[0,1] = 2  → 位置1应该预测token_2
# labels[0,2] = 3  → 位置2应该预测token_3

# CrossEntropyLoss 期望输入 (N, C) 和 (N,)
# N = batch × seq = 1 × 3 = 3  (总共3个预测位置)
# C = vocab = 5  (5个候选词)
#
# reshape 后:
#   logits: (1, 3, 5) → (3, 5)
#   labels: (1, 3)    → (3,)
#
# 相当于把3个位置的预测堆叠在一起，一次性算3个loss然后取平均
auto_loss = F.cross_entropy(
    logits.reshape(-1, vocab),   # shape: (3, 5)
    labels.reshape(-1)           # shape: (3,)
)
# auto_loss 是一个标量，表示3个位置的平均loss

# ============================================================================
# 形状变化总结
# ============================================================================
# input_ids: (1, 3)              ← 3个token的id
#     ↓ Embedding
# x:         (1, 3, 8)           ← 3个token的向量
#     ↓ Attention
# q/k/v:     (1, 3, 8)           ← 3个位置的query/key/value
#     ↓ q @ k.T
# scores:    (1, 3, 3)           ← 3×3注意力分数
#     ↓ Mask + Softmax
# attn:      (1, 3, 3)           ← 3×3注意力权重
#     ↓ attn @ v
# out:       (1, 3, 8)           ← 3个位置的attention输出
#     ↓ FFN
# ffn:       (1, 3, 8)           ← 3个位置的FFN输出
#     ↓ Output Projection
# logits:    (1, 3, 5)           ← 3个位置 × 5个词 的分数
#     ↓ CrossEntropyLoss
# loss:      标量                ← 3个位置的平均loss
#
# 关键洞察: seq维度(3)从头到尾都在！
# 矩阵乘法 @ 天然并行处理所有行，没有for循环！
