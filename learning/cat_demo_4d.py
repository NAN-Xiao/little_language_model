"""
torch.cat 在 4 维张量上的用法演示 —— 用具体数值，不用随机数
==================================================================

用 RGB 3 通道图像举例，拼接前后每个数的变化一目了然。
所有 cat 结果都在注释中以分解形式展示，不运行也能看懂。
"""

import torch


# ═══════════════════════════════════════════════════════════════════════════
# 输入数据
# ═══════════════════════════════════════════════════════════════════════════

# img_a: 2 张图，每张 RGB 3 通道，2x3 像素
# 数值带 1xx 前缀，一眼认出
img_a = torch.tensor([
    [   # batch 0
        [[101, 102, 103], [104, 105, 106]],   # ch0 (R)
        [[111, 112, 113], [114, 115, 116]],   # ch1 (G)
        [[121, 122, 123], [124, 125, 126]],   # ch2 (B)
    ],
    [   # batch 1
        [[131, 132, 133], [134, 135, 136]],   # ch0 (R)
        [[141, 142, 143], [144, 145, 146]],   # ch1 (G)
        [[151, 152, 153], [154, 155, 156]],   # ch2 (B)
    ],
])

# img_b: 同上结构，数值带 2xx 前缀
img_b = torch.tensor([
    [   # batch 0
        [[201, 202, 203], [204, 205, 206]],   # ch0 (R)
        [[211, 212, 213], [214, 215, 216]],   # ch1 (G)
        [[221, 222, 223], [224, 225, 226]],   # ch2 (B)
    ],
    [   # batch 1
        [[231, 232, 233], [234, 235, 236]],   # ch0 (R)
        [[241, 242, 243], [244, 245, 246]],   # ch1 (G)
        [[251, 252, 253], [254, 255, 256]],   # ch2 (B)
    ],
])

# img_a shape: (2, 3, 2, 3) = (B=2, C=3, H=2, W=3)
# img_b shape: (2, 3, 2, 3)


# ═══════════════════════════════════════════════════════════════════════════
# cat(dim=0): batch 维度拼接 → 图片数量翻倍
# ═══════════════════════════════════════════════════════════════════════════

result_0 = torch.cat([img_a, img_b], dim=0)
# shape: (4, 3, 2, 3)
#
# 分解:
#   batch 0:                    ← 来自 img_a
#     ch0 (R): [[101, 102, 103], [104, 105, 106]]
#     ch1 (G): [[111, 112, 113], [114, 115, 116]]
#     ch2 (B): [[121, 122, 123], [124, 125, 126]]
#   batch 1:                    ← 来自 img_a
#     ch0 (R): [[131, 132, 133], [134, 135, 136]]
#     ch1 (G): [[141, 142, 143], [144, 145, 146]]
#     ch2 (B): [[151, 152, 153], [154, 155, 156]]
#   batch 2:                    ← 来自 img_b
#     ch0 (R): [[201, 202, 203], [204, 205, 206]]
#     ch1 (G): [[211, 212, 213], [214, 215, 216]]
#     ch2 (B): [[221, 222, 223], [224, 225, 226]]
#   batch 3:                    ← 来自 img_b
#     ch0 (R): [[231, 232, 233], [234, 235, 236]]
#     ch1 (G): [[241, 242, 243], [244, 245, 246]]
#     ch2 (B): [[251, 252, 253], [254, 255, 256]]
#
# 解读: 4 张图叠成一摞，前 2 张是 img_a (1xx)，后 2 张是 img_b (2xx)


# ═══════════════════════════════════════════════════════════════════════════
# cat(dim=1): channel 维度拼接 → 通道数翻倍
# ═══════════════════════════════════════════════════════════════════════════

result_1 = torch.cat([img_a, img_b], dim=1)
# shape: (2, 6, 2, 3)
#
# 分解:
#   batch 0:
#     ch0 (R): [[101, 102, 103], [104, 105, 106]]   ← 来自 img_a
#     ch1 (G): [[111, 112, 113], [114, 115, 116]]   ← 来自 img_a
#     ch2 (B): [[121, 122, 123], [124, 125, 126]]   ← 来自 img_a
#     ch3 (R): [[201, 202, 203], [204, 205, 206]]   ← 来自 img_b
#     ch4 (G): [[211, 212, 213], [214, 215, 216]]   ← 来自 img_b
#     ch5 (B): [[221, 222, 223], [224, 225, 226]]   ← 来自 img_b
#   batch 1:
#     ch0 (R): [[131, 132, 133], [134, 135, 136]]   ← 来自 img_a
#     ch1 (G): [[141, 142, 143], [144, 145, 146]]   ← 来自 img_a
#     ch2 (B): [[151, 152, 153], [154, 155, 156]]   ← 来自 img_a
#     ch3 (R): [[231, 232, 233], [234, 235, 236]]   ← 来自 img_b
#     ch4 (G): [[241, 242, 243], [244, 245, 246]]   ← 来自 img_b
#     ch5 (B): [[251, 252, 253], [254, 255, 256]]   ← 来自 img_b
#
# 解读: 每张图变成 6 通道，ch 0-2 是 img_a (1xx)，ch 3-5 是 img_b (2xx)


# ═══════════════════════════════════════════════════════════════════════════
# cat(dim=2): height 维度拼接 → 图变高（上下拼）
# ═══════════════════════════════════════════════════════════════════════════

result_2 = torch.cat([img_a, img_b], dim=2)
# shape: (2, 3, 4, 3)
#
# 分解:
#   batch 0:
#     ch0 (R): [[101, 102, 103], [104, 105, 106],    ← 来自 img_a (上行)
#               [201, 202, 203], [204, 205, 206]]    ← 来自 img_b (下行)
#     ch1 (G): [[111, 112, 113], [114, 115, 116],
#               [211, 212, 213], [214, 215, 216]]
#     ch2 (B): [[121, 122, 123], [124, 125, 126],
#               [221, 222, 223], [224, 225, 226]]
#   batch 1:
#     ch0 (R): [[131, 132, 133], [134, 135, 136],
#               [231, 232, 233], [234, 235, 236]]
#     ch1 (G): [[141, 142, 143], [144, 145, 146],
#               [241, 242, 243], [244, 245, 246]]
#     ch2 (B): [[151, 152, 153], [154, 155, 156],
#               [251, 252, 253], [254, 255, 256]]
#
# 解读: 高变成 4，上行来自 img_a (1xx)，下行来自 img_b (2xx)，上下叠在一起


# ═══════════════════════════════════════════════════════════════════════════
# cat(dim=3): width 维度拼接 → 图变宽（左右拼）
# ═══════════════════════════════════════════════════════════════════════════

result_3 = torch.cat([img_a, img_b], dim=3)
# shape: (2, 3, 2, 6)
#
# 分解:
#   batch 0:
#     ch0 (R): [[101, 102, 103, 201, 202, 203],     ← 左 3 列 img_a + 右 3 列 img_b
#               [104, 105, 106, 204, 205, 206]]
#     ch1 (G): [[111, 112, 113, 211, 212, 213],
#               [114, 115, 116, 214, 215, 216]]
#     ch2 (B): [[121, 122, 123, 221, 222, 223],
#               [124, 125, 126, 224, 225, 226]]
#   batch 1:
#     ch0 (R): [[131, 132, 133, 231, 232, 233],
#               [134, 135, 136, 234, 235, 236]]
#     ch1 (G): [[141, 142, 143, 241, 242, 243],
#               [144, 145, 146, 244, 245, 246]]
#     ch2 (B): [[151, 152, 153, 251, 252, 253],
#               [154, 155, 156, 254, 255, 256]]
#
# 解读: 宽变成 6，左 3 列来自 img_a (1xx)，右 3 列来自 img_b (2xx)，左右拼在一起


# ═══════════════════════════════════════════════════════════════════════════
# 实际例子: Transformer generate() 中的拼接
# ═══════════════════════════════════════════════════════════════════════════

generated = torch.tensor([
    [1, 3, 12],    # batch 0: [BOS, "今", "天"]
    [1, 5, 7],     # batch 1: [BOS, "你", "好"]
])  # shape: (2, 3)

next_token = torch.tensor([
    [25],   # batch 0: 预测出 "天"
    [8],    # batch 1: 预测出 "气"
])  # shape: (2, 1)

result = torch.cat([generated, next_token], dim=1)
# shape: (2, 4)
#
# 分解:
#   [1, 3, 12, 25]   ← batch 0: "今天天"
#   [1, 5, 7, 8]     ← batch 1: "你好气"
#
# 解读: 新 token 接到每行末尾，序列长度 +1
